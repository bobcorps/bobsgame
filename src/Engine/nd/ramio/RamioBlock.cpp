RamioBlock::RamioBlock(sp<Engine> g)
{ //=========================================================================================================================
	this->e = g;
}

void RamioBlock::update()
{ //=========================================================================================================================
	if (hit == true)
	{
		getHit();
	}
}

void RamioBlock::getHit()
{ //=========================================================================================================================
}

void RamioBlock::checkHit(float x, float y)
{ //=========================================================================================================================

	if (x >= getLeft() && x <= getRight() && y >= getTop() && y <= getBottom())
	{
		hit = true;
	}
}

